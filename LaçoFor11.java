public class LaçoFor11 {
        public static void main(String[] args) {
            System.out.println("Quadrados dos números de 1 a 10:");

            for (int i = 1; i <= 10; i++) {
               // Calcula o quadrado do número
                int quadrado = i * i;
                // Exibe o quadrado
                System.out.println(i + "² = " + quadrado);
            }
        }
    }

