package LaçoFORDoWhile;

public class LaçoDOWHILE33 {
        public static void main(String[] args) {
            int i = 1;

            do {
                System.out.println(i);

                // Incrementamos o valor de 'i' em 1.
                // Isso garante que o próximo número seja exibido na próxima iteração.
                i++;

                // Aqui verificamos a condição para continuar o loop.
            } while (i <= 10); // O loop continua enquanto 'i' for menor ou igual a 10.

            // Quando 'i' for maior que 10, o loop termina e o programa segue adiante.
        }
    }

