public class LaçoFor12 {
        public static void main(String[] args) {
            System.out.println("Cubos dos números de 1 a 10:");

            for (int i = 1; i <= 10; i++) {
                // Calcula o cubo do número
                int cubo = i * i * i;
                // Exibe o cubo
                System.out.println(i + "³ = " + cubo);
            }
        }
    }

