public class LaçoFor20 {
        public static void main(String[] args) {
            System.out.println("Números de 1 a 50 pulando de 5 em 5:");

            for (int i = 1; i <= 50; i += 5) {
                System.out.print(i + " "); // Exibe o número atual
            }
        }
    }

